num = [2,9,7]
fruits = ["banana","strawberry","apple"]

#appending
num.append(4)
print(num)

#sorting
num.sort()
fruits.sort()
print(fruits)
print(num)

#Descending order
num.sort(reverse = True)
print(num)

#reversing 
fruits.reverse()
print(fruits)

#insert insert(index,element)
num.insert(0,8)
print(num)

num.remove(9)
print(num)
